# –î–æ–±–∞–≤–ª—è–µ–º –≤—Å–µ —Ñ–∞–π–ª—ã (–∫—Ä–æ–º–µ .env, –æ–Ω –≤ .gitignore)
echo -e "${GREEN}‚ûï –î–æ–±–∞–≤–ª—è–µ–º —Ñ–∞–π–ª—ã...${NC}"
git add .

# –î–æ–ø–æ–ª–Ω–∏—Ç–µ–ª—å–Ω–∞—è –ø—Ä–æ–≤–µ—Ä–∫–∞: —É–±–µ–∂–¥–∞–µ–º—Å—è, —á—Ç–æ .env –Ω–µ –ø–æ–ø–∞–ª –≤ staging
if git diff --cached --name-only | grep -q "^\.env$"; then
    echo -e "${RED}‚ùå –û–®–ò–ë–ö–ê: –§–∞–π–ª .env –ø–æ–ø–∞–ª –≤ staging area!${NC}"
    echo -e "${YELLOW}‚ö†Ô∏è  –£–¥–∞–ª—è–µ–º .env –∏–∑ staging...${NC}"
    git reset HEAD .env
    echo -e "${RED}‚ùå –î–µ–ø–ª–æ–π –æ—Ç–º–µ–Ω–µ–Ω –∏–∑ —Å–æ–æ–±—Ä–∞–∂–µ–Ω–∏–π –±–µ–∑–æ–ø–∞—Å–Ω–æ—Å—Ç–∏${NC}"
    exit 1
fi

# –ü–æ–ª—É—á–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏–µ –∫–æ–º–º–∏—Ç–∞
if [ -z "$1" ]; then
    read -p "–í–≤–µ–¥–∏—Ç–µ –æ–ø–∏—Å–∞–Ω–∏–µ –∫–æ–º–º–∏—Ç–∞: " commit_message
else
    commit_message="$1"
fi

# –ö–æ–º–º–∏—Ç
echo -e "${GREEN}üíæ –°–æ–∑–¥–∞–µ–º –∫–æ–º–º–∏—Ç...${NC}"
